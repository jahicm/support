package org.report.xml.support;

public class SixModel {
	
	private String lsvIdentAddresses;
	private String name;
	private String annotation;
	private String street;
	private String postalCode;
	private String place;
	private String country;
	private String csCountryCd;
	private String isoCountryCd;
	private String postBox;
	
	
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getCsCountryCd() {
		return csCountryCd;
	}
	public void setCsCountryCd(String csCountryCd) {
		this.csCountryCd = csCountryCd;
	}
	public String getIsoCountryCd() {
		return isoCountryCd;
	}
	public void setIsoCountryCd(String isoCountryCd) {
		this.isoCountryCd = isoCountryCd;
	}
	public String getPostBox() {
		return postBox;
	}
	public void setPostBox(String postBox) {
		this.postBox = postBox;
	}
	public String getLsvIdentAddresses() {
		return lsvIdentAddresses;
	}
	public void setLsvIdentAddresses(String lsvIdent) {
		this.lsvIdentAddresses = lsvIdent;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAnnotation() {
		return annotation;
	}
	public void setAnnotation(String annotation) {
		this.annotation = annotation;
	}
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		this.street = street;
	}
	public String getPostalCode() {
		return postalCode;
	}
	public void setPostalCode(String postalCode) {
		this.postalCode = postalCode;
	}
	public String getPlace() {
		return place;
	}
	public void setPlace(String place) {
		this.place = place;
	}

}
